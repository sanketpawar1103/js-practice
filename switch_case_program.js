// Switch case program for the age of a person.
let ageOfPerson = 20;

switch(ageOfPerson){
  case 10: 
    console.log("Age is 10");
    break;
  case 20:
    console.log("Age is 20");
    break;
  case 30: 
    console.log("Age is 30");
    break;
  default:
    console.log("Invalid age value");
}

